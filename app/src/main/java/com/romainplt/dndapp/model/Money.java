package com.romainplt.dndapp.model;

public class Money {

    private int gold;
    private int platinium;
    private int electrum;
    private int silver;
    private int bronze;

    public int getGold() {
        return gold;
    }

    public void setGold(int gold) {
        this.gold = gold;
    }

    public int getPlatinium() {
        return platinium;
    }

    public void setPlatinium(int platinium) {
        this.platinium = platinium;
    }

    public int getElectrum() {
        return electrum;
    }

    public void setElectrum(int electrum) {
        this.electrum = electrum;
    }

    public int getSilver() {
        return silver;
    }

    public void setSilver(int silver) {
        this.silver = silver;
    }

    public int getBronze() {
        return bronze;
    }

    public void setBronze(int bronze) {
        this.bronze = bronze;
    }
}
